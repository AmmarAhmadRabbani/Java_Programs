class Operators
{
public static void main(String[] args)
{
int p=12;
int q=2;
System.out.println(p);
System.out.println(q);
p+=q;
System.out.println(p);
System.out.println(q);
p*=q;
System.out.println(p);
System.out.println(q);
p/=q;
System.out.println(p);
System.out.println(q);
p%=q;
System.out.println(p);
System.out.println(q);
p-=q;
System.out.println(p);
System.out.println(q);
}
}